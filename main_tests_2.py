def expanded_form(num):
    lst = reversed(list(str(num)))
    fil = filter(lambda x: x != '0', [str(int(v) * (1, 10 ** k)[k>0]) for k, v in enumerate(lst)])
    return ' + '.join(reversed(list(fil)))

if __name__ == '__main__':
    print(expanded_form(70304))



